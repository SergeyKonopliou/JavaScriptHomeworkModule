//Задание 1 Запросить у пользователя его возраст и определить, кем он является: ребенком (0–12),
//подростком (12–17), взрослым(18_60) или пенсионером(61– ...).
let userAge = prompt("Сколько вам лет?");
if (userAge >= 0 && userAge < 13) {
    alert("Ребёнок");
} else if (userAge >= 13 && userAge < 18) {
    alert("Подросток");
} else if (userAge >= 18 && userAge < 61) {
    alert("Взрослый");
} else {
    alert("Пенсионер");
}

//Задание 2 Запросить у пользователя число от 0 до 9 и вывести ему спецсимвол,
//который расположен на этой клавише (1–!,2–@, 3–# и т. д).
let specialSymbol = prompt("Введите число от 0 до 9");
switch (+specialSymbol) {
    case 0:
        alert(')');
        break;
    case 1:
        alert('!');
        break;
    case 2:
        alert('@');
        break;
    case 3:
        alert('#)');
        break;
    case 4:
        alert('$');
        break;
    case 5:
        alert('%');
        break;
    case 6:
        alert('^');
        break;
    case 7:
        alert('&)');
        break;
    case 8:
        alert('*');
        break;
    case 9:
        alert('(');
        break;
    default:
        alert('Неверный ввод');
}


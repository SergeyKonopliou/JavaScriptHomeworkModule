//есть ли в нем одинаковые цифры
let num = prompt("Введите трёхзначное число");
let firstDigit = num % 10;
let secondDigit = Math.floor(firstDigit / 10) % 10;
let thirdDigit = Math.floor(num / 100);
if (firstDigit == secondDigit || firstDigit == thirdDigit || secondDigit == thirdDigit) {
    alert("В числе есть одинаковые цифры");
} else {
    alert("В числе нет одинаковых цифр");
}


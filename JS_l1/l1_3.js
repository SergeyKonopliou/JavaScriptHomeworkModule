//Задание 5 Запросить у пользователя 10 чисел и подсчитать, сколько
//он ввел положительных, отрицательных и нулей. При этом
//также посчитать, сколько четных и нечетных. Вывести
//статистику на экран. Учтите, что достаточно одной переменной (не 10) для ввода чисел пользователем.
let positiveNumberCount = 0;
let negativeNumberCount = 0;
let nullNumberCount = 0;
let evenNumberCount = 0;
let unevenNumberCount = 0;
for (let i = 0; i < 10; i++) {
    let n = prompt("Введите число");
    if (n > 0) positiveNumberCount++;
    else if (n < 0) negativeNumberCount++;
    else nullNumberCount++;
    if (n % 2 == 0) evenNumberCount++;
    else unevenNumberCount++;
}
alert("Положительных чисел: " + positiveNumberCount + "\n Отрицательных чисел: " + negativeNumberCount +
    "\n Нулей: " + nullNumberCount + "\n Четных чисел: " + evenNumberCount + "\n Нечетных чисел: " + unevenNumberCount);

//Задание 6 Зациклить калькулятор. Запросить у пользователя 2 числа
//и знак, решить пример, вывести результат и спросить, хочет ли он решить еще один пример. И так до тех пор, пока пользователь не откажется.
let query;
do {
    let n1 = prompt("Введите первое число");
    let n2 = prompt("Введите второе число");
    let s = prompt("Введите знак");
    switch (s) {
        case "+":
            alert(+n1 + +n2);
            break;
        case "-":
            alert(+n1 - +n2);
            break;
        case "*":
            alert(+n1 * +n2);
            break;
        case "/":
            alert(+n1 / +n2);
            break;
    }
    query = confirm("Хотите решить ещё пример?");
}
while (query);

//Задание 1 Подсчитать сумму всех чисел в заданном пользователем диапазоне.
let firstN = prompt("Введите начало диапазона");
let secondN = prompt("Введите конец диапазона");
let sum = 0;
let f = +firstN;
let s = +secondN;
for (f; f <= s; f++) {
    sum += f;
}
alert(sum);

//Задание 2 Запросить 2 числа и найти только наибольший общий делитель.
//Пример:
//Найти НОД для 30 и 18.
//30 / 18 = 1 (остаток 12)
//18 / 12 = 1 (остаток 6)
//12 / 6 = 2 (остаток 0)
//Конец: НОД – это делитель 6.
//НОД (30, 18) = 6
//В цикле в переменную a или b записывается остаток от деления. Цикл завершается, когда хотя бы одна из переменных равна нулю. Это значит, что другая содержит НОД. Однако какая именно, мы не знаем. Поэтому для НОД находим сумму этих переменных. Поскольку в одной из переменных ноль, он не оказывает влияние на результат.
let a = prompt("Введите первое число");
let b = prompt("Введите второе число");
while (a != 0 && b != 0) {
    if (a > b) {
        a = a % b
    } else {
        b = b % a;
    }
}
alert(+a + +b);

//Задание 3 Запросить у пользователя число и вывести все делителии этого числа.
let userNumber = prompt("Введите число");
let res = "";
for (let i = 1; i <= userNumber; i++) {
    if (userNumber % i == 0) {
        res += i + " ";
    }
}
alert(res);

